#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	out_put;

	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		n *= -1;
	}
	if (10 <= n)
		ft_putnbr_fd(n / 10, fd);
	out_put = n % 10 + '0';
	write(fd, &out_put, 1);
}
